//Faça um programa que receba dois números e ao final mostre a soma, subtração, multiplicação e a divisão dos números lidos.

public class ex2 {

    public static void main(String[] args) {

        int x = 1;
        int y = 2;
        int soma = x + y;
        int subtração = x - y;
        int multiplicação = x * y;
        float divisão = (float) x / (float) y;

        System.out.println("A Soma: " + soma);
        System.out.println("A Subtração: " + subtração);
        System.out.println("A Multiplicação: " + multiplicação);
        System.out.println("A Divisão: " + divisão);

    }
    
}
